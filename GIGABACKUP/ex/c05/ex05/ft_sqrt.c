int	ft_sqrt(int nb)
{
	int	a;
	int	b;

	b = nb;
	a = 1;
	if (nb < 1)
		return (0);
	while (b > a)
	{
		b = (b + a) / 2;
		a = nb / b;
	}
	if (b * b == nb)
		return (b);
	else
		return (0);
}
